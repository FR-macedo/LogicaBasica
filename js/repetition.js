// For loop demonstration
console.log("For loop:");
for (let i = 0; i < 5; i++) {
    console.log(`i: ${i}`);
}

// While loop demonstration
console.log("\nWhile loop:");
let j = 0;
while (j < 5) {
    console.log(`j: ${j}`);
    j++;
}

// Do-while loop demonstration
console.log("\nDo-while loop:");
let k = 0;
do {
    console.log(`k: ${k}`);
    k++;
} while (k < 5);
